public class Entity 
{
  private double x, y;
  public Entity(double x, double y)
  {
    this.x = x;
    this.y = y;
  }
  public double x()
  {
    return x;
  }
  public double y()
  {
    return y;
  }
  public void setX(double x)
  {
    this.x = x;
  }
  public void setY(double y)
  {
    this.y = y;
  }
}

